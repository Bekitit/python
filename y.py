# len to count letters inside string
name = 'Beka'
print(len(name))

# isalpha()  True if all characters are alphabetic
name1 = 'Beka'
print(name1.isalpha())

# upper() output all the string as an upper case
name2 = 'Beka'
print(name2.upper())

# isdigit() it tells if the string is digits
price = '2233435'
print(price.isdigit())

# isalnum() it tells if its alphanumeric
name3 = 'Abel'
print(name3.isalnum())

# capitalize()  capitalize the first letter of the string
name4 = 'beka'
print(name4.capitalize())

# islower() to see if all letters are in lowerCase 
name5 = 'beka'
print(name5.islower())